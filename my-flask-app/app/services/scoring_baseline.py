def get_make_baseline(make: str) -> int:
    """Return the make-level default reliability score (0-100).

    Lookup is case-insensitive. Returns 72 when the make is not found.
    """
    return _MAKE_DEFAULTS.get(make.lower().strip(), 72)


def get_model_baseline(make: str, model: str) -> int:
    """Return the model-specific baseline score, falling back to make level.

    Strips trailing year-range suffixes such as "(1992-2026)" from *model*
    before matching. Tries exact match first, then case-insensitive exact,
    then partial substring match against the stored keys.
    """
    clean = _YEAR_RANGE_RE.sub("", model).strip()

    # Locate the make's model dict (case-insensitive make lookup)
    make_models: Optional[dict[str, int]] = MODEL_OVERRIDES.get(make)
    if make_models is None:
        make_lower = make.lower()
        for k, v in MODEL_OVERRIDES.items():
            if k.lower() == make_lower:
                make_models = v
                break

    if make_models is not None:
        # 1. Exact match
        if clean in make_models:
            return make_models[clean]

        # 2. Case-insensitive exact match
        clean_lower = clean.lower()
        for key, score in make_models.items():
            if key.lower() == clean_lower:
                return score

        # 3. Partial/substring match (prefer key that contains clean, or clean contains key)
        for key, score in make_models.items():
            key_lower = key.lower()
            if clean_lower in key_lower or key_lower in clean_lower:
                return score

    return get_make_baseline(make)


def lookup_override(make: str, model: str, year: Optional[int] = None) -> int:
    """Return the final baseline score with optional age penalty, clamped to [35, 95].

    Age penalty (applied when *year* is not None):
      - year < 2000 → subtract 5
      - 2000 ≤ year < 2005 → subtract 3
      - year ≥ 2005 → no penalty
    """
    score = get_model_baseline(make, model)
    if year is not None:
        if year < 2000:
            score -= 5
        elif year < 2005:
            score -= 3
    return max(35, min(95, score))
